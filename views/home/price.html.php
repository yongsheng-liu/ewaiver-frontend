            $(e).removeClass().addClass(tArr[i]);
        })
        index--;
        if (index<0) {
            index=5;
        }
    }

    function previmgT(){
        tArr.push(tArr[0]);        
        tArr.shift();
        $(".slideBox li, .businessCases li").each(function(i,e){
            $(e).removeClass().addClass(tArr[i]);
        })
        index++;
        if (index>5) {
            index=0;
        }
